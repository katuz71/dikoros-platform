export default function PoliciesScreen() {
  const router = useRouter();
  const goProfile = () => router.replace('/(tabs)/profile' as any);
  const insets = useSafeAreaInsets();
  const headerTopInset = Math.max(insets.top, 18);
  const params = useLocalSearchParams<{ page?: string }>();
  const pageKey = String(params.page || 'privacy');
  const page = PAGES[pageKey] || PAGES.privacy;
  const isFaqPage = pageKey === 'faq';
  const [expandedFaq, setExpandedFaq] = React.useState<Record<number, boolean>>({});

  const toggleFaq = (index: number) => {
    setExpandedFaq(prev => ({ ...prev, [index]: !prev[index] }));
  };

  return (
    <SafeAreaView style={styles.safe}>
      <View style={[styles.header, { height: 62 + headerTopInset, paddingTop: headerTopInset }]}>
        <TouchableOpacity onPress={goProfile} style={styles.backBtn}>
          <Ionicons name="arrow-back" size={26} color="#111" />
        </TouchableOpacity>
        <Text style={styles.headerTitle}>{page.title}</Text>
        <View style={{ width: 42 }} />
      </View>

      <ScrollView contentContainerStyle={styles.content}>
        {page.sections.map((section, index) => {
          if (isFaqPage) {
            const isOpen = !!expandedFaq[index];

            return (
              <View key={index} style={styles.faqCard}>
                <TouchableOpacity
                  style={styles.faqQuestionRow}
                  onPress={() => toggleFaq(index)}
                  activeOpacity={0.75}
                >
                  <Text style={styles.faqQuestion}>{section.h}</Text>
                  <Ionicons
                    name={isOpen ? 'chevron-up' : 'chevron-down'}
                    size={21}
                    color="#2E7D32"
                  />
                </TouchableOpacity>

                {isOpen && (
                  <View style={styles.faqAnswer}>
                    {section.p.map((text, i) => (
                      <Text key={i} style={styles.paragraph}>{text}</Text>
                    ))}
                  </View>
                )}
              </View>
            );
          }

          return (
            <View key={index} style={styles.card}>
              <Text style={styles.sectionTitle}>{section.h}</Text>
              {section.p.map((text, i) => (
                <Text key={i} style={styles.paragraph}>{text}</Text>
              ))}
            </View>
          );
        })}

        {pageKey === 'contacts' && (
          <View style={styles.card}>
            <Text style={styles.sectionTitle}>Швидкий зв’язок</Text>
            {CONTACT_ACTIONS.map((action) => (
              <TouchableOpacity
                key={action.url}
                style={styles.contactActionBtn}
                onPress={() => Linking.openURL(action.url)}
              >
                <Ionicons name={action.icon as any} size={20} color="#2E7D32" />
                <Text style={styles.contactActionText}>{action.label}</Text>
              </TouchableOpacity>
            ))}
          </View>
        )}

      </ScrollView>
    </SafeAreaView>
  );
}

